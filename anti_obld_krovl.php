<p><strong>Система управления.</strong> Вся управляющая и коммутирующая аппаратура располагается в отдельном распределительном шкафу, удобно расположенным для контроля за системой и ее обслуживанием.
Обязательно наличие дифференциального реле (Устройства Защитного Отключения). Датчики температуры воздуха располагаются в северной части кровли, вдали от воздуховодов системы отопления или кондиционирования, в тени. Датчики влажности (осадков) должны располагаться в местах, где наличие влаги наиболее вероятно при снеге или дожде и в момент работы системы до полного удаления льда из водостоков. Вариант: в водосточном лотке около слива в водосточную трубу. 
</p>

<p><strong>Распределительная сеть.</strong> Каждый отдельный комплект кабеля и датчик подключается к своей распределительной коробке, которая может располагаться либо на кровле, <img src="Image/krovl5.jpg" align="Right" border="0" vspace="10" hspace="10">либо на стене здания. В зависимости от функционального назначения здания и условий его эксплуатациии, возможна нижняя (распределительные коробки крепятся у нижнего основания водосточных труб) или верхняя (распределительные коробки крепятся на кровле или на стене возле водосточного желоба) силовая разводка. Распределительные коробки соединяются с распределительным шкафом силовым электрическим кабелем соответствующего сечения. Недопустимо одновременное использование одного многожильного кабеля для силовой и информационной проводки во избежание искажений показаний датчиков.
</p>
<p>Монтаж системы условно делится на несколько этапов:
<ul>
<li>проектирование и согласование вариантов системы
<li>устройство распределительной сети (до чистовой отделки стен и установки верхнего слоя кровли)
<li>установка распределительного шкафа без аппаратуры
<li>монтаж нагревательных кабелей и датчиков (после полного монтажа кровли и водосточной системы)
<li>установка управляющей и коммутирующей аппаратуры
<li>испытание и запуск системы
</ul>
</p>
<p>В начале каждого осенне-зимнего сезона необходимо проведение пробного запуска и проверки работоспособности антиобледенительной системы в целом.
</p>

